class TestActiveLearner(unittest.TestCase):

    def setUp(self) -> None:
        torch.manual_seed(0)
        self.learner = ActiveLearner.digit_learner()

    def test_add_wrong_label(self):
        self.assertRaises(KeyError, self.learner.add_image, torch.Tensor(), 'does not exist')

    def test_add_image(self):
        image = torch.zeros((28, 28))
        label = '0'

        self.learner.add_image(image, label)

        self.assertEqual(len(self.learner.test_data[label].dataset), 1)

    def test_learn_zero(self):
        image, label = zero()

        self.learner.add_image(image, label)

        self.assertEqual(self.learner.classify(image), '4')

    def test_class_accuracy(self):
        image, _ = zero()

        # Add the same image with every label
        # The untrained network will predict the same number every time, so one of these will be right
        for i in range(10):
            self.learner.add_image(image, str(i))

        for i in range(10):
            # if i == 4:  # the untrained model picks 4
            #     self.assertEqual(self.learner.class_accuracy(str(i)), 1.)
            # else:
            self.assertEqual(self.learner.class_accuracy(str(i)), 0., i)
