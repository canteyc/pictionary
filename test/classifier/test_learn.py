    def test_accuracy(self):
        image, _ = zero()
        # Add the same image with every label
        # The untrained network will predict the same number every time, so one of these will be right
        for i in range(10):
            self.learner.test_data.dataset.append(image.unsqueeze(0), str(i))
        expected = [0.] * 10
        expected[7] = 1.  # the initial model thinks this is a 7

        accuracy = self.learner.accuracy()

        self.assertSequenceEqual(accuracy, expected)

    def test_setting_sampler_weights(self):
        image, _ = zero()
        image = image.unsqueeze(0)
        self.learner.test_data.dataset.append(image, '7')
        self.learner.test_data.dataset.append(torch.zeros_like(image), '7')  # this should get the accuracy down to 0.5

        self.learner._reset_training_sampler()
        expected_weights = self.learner.weights
        actual_weights = self.learner.training_data.sampler.weights

        self.assertTrue(all(actual_weights == expected_weights))

